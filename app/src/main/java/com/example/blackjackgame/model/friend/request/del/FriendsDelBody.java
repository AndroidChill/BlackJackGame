package com.example.blackjackgame.model.friend.request.del;

import com.google.gson.annotations.SerializedName;

public class FriendsDelBody {

    @SerializedName("menu")
    private String menu;

    @SerializedName("status")
    private String status;

    @SerializedName("toast")
    private String toast;

    @SerializedName("time_server")
    private long time_server;

    public FriendsDelBody(String menu, String status, String toast, long time_server) {
        this.menu = menu;
        this.status = status;
        this.toast = toast;
        this.time_server = time_server;
    }

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getToast() {
        return toast;
    }

    public void setToast(String toast) {
        this.toast = toast;
    }

    public long getTime_server() {
        return time_server;
    }

    public void setTime_server(long time_server) {
        this.time_server = time_server;
    }
}
